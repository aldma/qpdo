qpdo_src_path = '../../src';
for i = 1:length (qpdo_src)
    qpdo_src {i} = [qpdo_src_path '/' qpdo_src{i}] ;
end

if (pc)
    % Windows does not have drand48 and srand48, required by METIS.  Use
    % drand48 and srand48 in CHOLMOD/MATLAB/Windows/rand48.c instead.
    % Also provide Windows with an empty <strings.h> include file.
    obj_extension = '.obj' ;
    cholmod_matlab = [cholmod_matlab {'Windows/rand48'}] ;
    include = [include ' -IWindows'] ;
else
    obj_extension = '.o' ;
end

 % compile each library source file
obj = '' ;

source = [ordering_src config_src cholmod_src cholmod_matlab qpdo_src] ;
if (have_metis)
    source = [metis_src source] ;
end

kk = 0 ;
cflags = 'CFLAGS="\$CFLAGS -std=c99 -fPIC -DMATLAB -O3 -DPROFILING -DPRINTING"';
flags = [cflags ' ' flags];

for f = source
    ff = f {1} ;
    if (isequal (ff, [metis_path '/GKlib/util']))
        % special case, since a file with the same name also exists in `libmetis`
        copyfile ([ff '.c'], 'GKlib_util.c', 'f') ;
        ff = 'GKlib_util' ;
        o = 'GKlib_util' ;
    elseif (isequal (ff, [metis_path '/GKlib/graph']))
        % special case, since a file with the same name also exist in `libmetis`
        copyfile ([ff '.c'], 'GKlib_graph.c', 'f') ;
        ff = 'GKlib_graph' ;
        o = 'GKlib_graph' ;
    elseif (isequal (ff, [qpdo_src_path '/util']))
        % special case, since a file with the same name also exists in `qpdo`
        copyfile ([ff '.c'], 'qpdo_util.c', 'f') ;
        ff = 'qpdo_util' ;
        o = 'qpdo_util' ;
    else
        slash = strfind (ff, '/') ;
        if (isempty (slash))
            slash = 1 ;
        else
            slash = slash (end) + 1 ;
        end
        o = ff (slash:end) ;
    end
    o = [o obj_extension] ;%#ok
    obj = [obj  ' ' o] ;					            %#ok
    s = sprintf ('mex %s -DDLONG -O %s -c %s.c', flags, include, ff) ;
    s = [s obj ' ' lapack] ;%#ok
    kk = do_cmd(s, kk);
end

% compile!
s = sprintf ('mex %s -DDLONG -O %s %s.c', flags, include, 'qpdo_mex') ;
s = [s obj ' ' lapack];
kk = do_cmd(s, kk);

% clean up
s = ['delete ' obj];
do_cmd(s, kk);
fprintf ('\nQPDO successfully compiled\n') ;

% remove the renamed METIS files, if they exist
if (exist ('GKlib_util.c', 'file'))
    delete ('GKlib_util.c') ;
end
if (exist ('GKlib_graph.c', 'file'))
    delete ('GKlib_graph.c') ;
end
if (exist ('qpdo_util.c', 'file'))
    delete ('qpdo_util.c') ;
end

end

% helper function to evaluate a command
function kk = do_cmd (s, kk)
%DO_CMD: evaluate a command, and print a "."
    if (mod (kk, 50) == 0)
        fprintf('\n');
    end
    kk = kk + 1 ;
    fprintf('.');
    eval(s);
end
