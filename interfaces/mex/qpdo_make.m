function qpdo_make ()
%QPDO_MAKE compiles the QPDO mexFunction

% relies on CHOLMOD and AMD and COLAMD, and optionally CCOLAMD, CAMD, and METIS.

fprintf('\n');

warning('off','MATLAB:mex:GccVersion_link')

% Change to directory containing this function
cd( fileparts( mfilename('fullpath') ) );

fprintf ('Compiling QPDO mex function\n')

try
    % ispc does not appear in MATLAB 5.3
    pc = ispc ;
    mac = ismac ;
catch
    % if ispc fails, assume we are on a Windows PC if it's not unix
    pc = ~isunix ;
    mac = 0 ;
end

flags = '' ;
is64 = contains(computer, '64');
if (is64)
    % 64-bit MATLAB
    flags = '-largeArrayDims' ;
end

% MATLAB 8.3.0 now has a -silent option to keep 'mex' from burbling too much
if (~verLessThan ('matlab', '8.3.0'))
    flags = ['-silent ' flags] ;
end

include = ['-I.',...
           ' -I../../include',...
           ' -I../../SuiteSparse/AMD/Include',...
           ' -I../../SuiteSparse/COLAMD/Include',...
           ' -I../../SuiteSparse/CCOLAMD/Include',...
           ' -I../../SuiteSparse/CAMD/Include',...
